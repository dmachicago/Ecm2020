
alter procedure ckProcExists (@ProcName as nvarchar(250))
as
begin

    if exists (Select name from sys.procedures where name = @ProcName)
	   print 'OK: ' + @ProcName ;
    else 
    begin
	   print 'EXEC ' + @ProcName ;
	   insert into TEMP_DropCmds (cmd) values ('EXEC ' + @ProcName) ;
    end 
end 

